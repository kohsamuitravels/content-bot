// פונקציה שמחזירה נושא לפי מספר ריצה (rotation)
export function getTopicForRun(runNumber) {
  const allTopics = [];

  // שטח את כל הנושאים עם מידע על קטגוריה
  for (const [catKey, catData] of Object.entries(CATEGORIES)) {
    for (let i = 0; i < catData.topics.length; i++) {
      allTopics.push({
        category: catKey,
        categorySlug: catData.wpSlug,
        categoryHebrew: catData.hebrewName,
        topic: catData.topics[i],
        topicIndex: i,
      });
    }
  }

  // רוטציה מחזורית
  const index = runNumber % allTopics.length;
  return allTopics[index];
}

export function getTotalTopics() {
  return Object.values(CATEGORIES).reduce((sum, cat) => sum + cat.topics.length, 0);
}
